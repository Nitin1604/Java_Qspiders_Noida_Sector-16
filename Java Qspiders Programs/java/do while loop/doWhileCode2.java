class doWhileCode2 {
    public static void main(String[] args) {
        System.out.println("**** Main starts here ****");
        
        int i = 1; 
        do {
            System.out.println("variable i is:" + i);
            i++;
        } while (i <= 10); // Check condition after each iteration
        
        System.out.println("**** Main ends here ****");
    }
}