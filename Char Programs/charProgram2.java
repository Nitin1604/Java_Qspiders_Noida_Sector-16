class charProgram2
{
	public static void main(String[] args)
	{
		System.out.println('@'); // @
		System.out.println('@'+10); // 64+10=74 | ASCII value of @ is 64
		System.out.println('@'>30); // 64>30 | true
		System.out.println('B'>'b'); // 66 > 98 | false
		System.out.println(2+2); // 4
		System.out.println('2'+2); // 50+2=52 | ASCII value of '2' is 50
		System.out.println('2'+'2'); // 50+50=100
		System.out.println('B'+'d'+10+'2'); // 66+100+10+50=226
	}
}
