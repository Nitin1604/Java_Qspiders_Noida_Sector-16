class escapeSequence
{
	public static void main(String[] args)
	{
		System.out.println("\"Java\" and \"JavaScript\" are different programming language.");
		System.out.println("Today is the day to learn \t\tJava and JavaScript");
		System.out.println("Tomorrow is \nJav\nSc\n\nript");
		System.out.println("\"");
		System.out.println('\'');
		System.out.println("Java and \\JavaScript\\ are fun");	
	}
}

